numero1 = int(input("Digite um numero: "))
numero2 = int(input("Digite segundo numero: "))

if (numero1 == numero2):
    print(f'Primeiro numero, {numero1} é igual ao segundo numero, {numero2}')

if (numero1!= numero2):
    print(f'Primeiro numero, {numero1} é diferente do segundo, {numero2}')

if (numero1 < numero2):
    print(f'Primeiro numero, {numero1} é menor que o segundo, {numero2}')

if (numero1 > numero2):
    print(f'Segundo numero, {numero2} é menor que o primeiro {numero1}')

if (numero1 <= numero2):
    print(f'Segundo numero {numero2} é maior ou igual a {numero1}')

if (numero1 >= numero2):
    print(f'Primeiro numero {numero1} é maior ou igual {numero2}')